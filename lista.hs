--d=√((x_2-x_1)²+(y_2-y_1)²) 

dist_pontos :: (Float, Float) -> (Float, Float) -> Float
dist_pontos (x1, y1) (x2, y2) = sqrt((x2 - x1)^2 + (y2 - y1)^2)

